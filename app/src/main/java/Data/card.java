package Data;

public class card {
    public int id;
    public String title;
    public int il;
    public int ilce;
    public int alanid;

    @Override
    public String toString() {
        return "card{" +
                "id=" + id +
                ", title='" + title + '\'' +
                ", il=" + il +
                ", ilce=" + ilce +
                ", alanid=" + alanid +
                '}';
    }
}
